function theBeatlesPlay(musicians, instruments){
  var emptyArr = [];
  for (var i = 0; i < musicians.length; i++){
    var muses = musicians[i];
    var instru = instruments[i];
    emptyArr.push(muses + " plays " + instru);
  }
  return emptyArr;
}

